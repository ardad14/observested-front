                        <tr v-for="(action, index) in this.generalAnalyticsData.customers" :key="index">
                            <td>{{ index }}</td>
                            <td>{{ action.id }}</td>
                            <td>{{ action.pivot.spend_money }}</td>
                            <td>{{ convertData(action.created_at) }}</td>
